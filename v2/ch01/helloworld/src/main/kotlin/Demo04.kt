package com.zhangdapeng

fun main() {
    // 基础数据类型
    // 1、字节类型
    val a: Byte = 127
    // 2、短整型
    val b: Short = 32767
    // 3、整型
    val c: Int = 2147483647
    // 4、长整型
    val d: Long = 9223372036854775807
    // 5、浮点型
    val e: Float = 3.4028235E38f
    // 6、双精度浮点型
    val f: Double = 1.7976931348623157E308
    // 7、布尔型
    val g: Boolean = true
    // 8、字符型
    val h: Char = 'a'
    // 9、字符串型
    val i: String = "hello world"

    // 输出
    println("字节类型：$a")
    println("短整型：$b")
    println("整型：$c")
    println("长整型：$d")
    println("浮点型：$e")
    println("双精度浮点型：$f")
    println("布尔型：$g")
    println("字符型：$h")
    println("字符串型：$i")
}